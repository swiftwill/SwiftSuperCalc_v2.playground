import UIKit

//  declare and initialize input variables
var a = 10
var b = 5
var c = 7
// variables for mathematical operations
var addresults = 0
var subresults = 0
var multresults = 0
var divresults = 0
var modresults = 0


// operator definitions for mathematical operations
